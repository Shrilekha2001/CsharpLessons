using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace LessonA.Day_1
{
    internal class Calculator
    {
        public static int Add(int p1, int p2) 
        { 
            return p1+p2;
        }
        public static int subtraction(int p1, int p2) 
        {
            return p1-p2;
        }
        public static int multiply(int p1, int p2)
        {
            return p1*p2;
        }
        public static float divide(float p1, float p2)
        {
            return p1/p2;
        }
    }
}
